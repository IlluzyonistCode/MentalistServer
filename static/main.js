async function verifyKey() {
    const apiKey = document.getElementById('apiKey').value.trim();
    const resultDiv = document.getElementById('verifyResult');
    const t = translations[currentLang];

    const SERVER_URL = window.location.origin;

    resultDiv.classList.add('hidden');
    resultDiv.className = 'verify-result';

    if (!apiKey) {
        resultDiv.className = 'verify-result error';
        resultDiv.innerHTML = `<div class='result-header'>${t.verificationFailed}</div><p>${t.errorEnterKey}</p>`;
        resultDiv.classList.remove('hidden');

        return;
    }

    resultDiv.innerHTML = `<div class='result-header'>${t.verifying}</div>`;
    resultDiv.classList.remove('hidden');

    try {
        const verifyRes = await fetch(`${SERVER_URL}/api/verify`, {
            method: 'POST',
            headers: { 
                'Content-Type': 'application/json'
            },
            body: JSON.stringify({
                api_key: apiKey
            })
        });

        if (!verifyRes.ok) throw new Error(`HTTP Error ${verifyRes.status}`);

        const data = await verifyRes.json();

        if (!data.valid) throw new Error(data.error || 'Invalid API key');

        const modulesList = data.modules || [];

        resultDiv.innerHTML = `
            <div class='result-header'>${t.verificationSuccessful}</div>
            <div class='result-details'>
                <div class='result-item'>
                    <span class='result-label'>${t.userId}</span>
                    <span class='result-value'>#${data.user_id}</span>
                </div>
                <div class='result-item'>
                    <span class='result-label'>${t.status}</span>
                    <span class='result-value'>${t.active}</span>
                </div>
            </div>
            <div class='module-access'>
                <div class='module-access-title'>${t.moduleAccess}</div>
                ${modulesList.map(module => `
                    <div class='access-item'>
                        <span class='access-icon granted'>✓</span>
                        <span>${module}</span>
                    </div>
                `).join('')}
            </div>`;
    } catch (error) {
        console.error('Verification Error:', error);

        resultDiv.className = 'verify-result error';
        resultDiv.innerHTML = `
            <div class='result-header'>${t.verificationFailed}</div>
            <div class='result-details'>
                <p>${error.message}</p> 
            </div>`;
    }
}

async function downloadFile(type) {
    const t = translations[currentLang];
    const SERVER_URL = window.location.origin;
    
    const messages = {
        cli: t.downloadingCli,
        gui: t.downloadingGui,
        mobile: t.downloadingMobile
    };

    try {
        alert(messages[type]);

        const response = await fetch(`${SERVER_URL}/api/update/check?build_type=${type}`);
        
        if (!response.ok) throw new Error(`Server error: ${response.status}`);

        const data = await response.json();

        if (!data.update_available && !data.latest_version) {
            alert(t.noVersionAvailable);

            return;
        }

        const version = data.latest_version || data.current_version;

        if (!version) {
            alert(t.noVersionAvailable);

            return;
        }

        const downloadUrl = `${SERVER_URL}/api/update/download/${version}?build_type=${type}`;

        const link = document.createElement('a');
        link.href = downloadUrl;
        link.download = '';

        document.body.appendChild(link);
        link.click();
        document.body.removeChild(link);
    } catch (error) {
        console.error('Download error:', error);

        alert(t.downloadError);
    }
}

